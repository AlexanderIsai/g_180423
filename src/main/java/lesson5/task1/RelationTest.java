package lesson5.task1;

public class RelationTest {

    public static void main(String[] args) {

        int a = 5;
        int b = 10;
        int c = 5;

        System.out.println("Результат сравнения а и b");
        System.out.println(a == b);

        System.out.println("Результат сравнения а и c");
        System.out.println(a == c);

        System.out.println("Результат сравнения а is not b");
        System.out.println(a != b);

        System.out.println("Результат сравнения а >= c");
        System.out.println(a >= c);

        System.out.println("Результат сравнения а > c");
        System.out.println(a > c);

    }
}
