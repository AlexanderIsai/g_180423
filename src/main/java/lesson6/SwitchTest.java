package lesson6;

public class SwitchTest {

    public static void main(String[] args) {

        int i = 18;

        switch (i) {
            case 1:
                System.out.println("i = 1");
                break;
            case 2:
                System.out.println("i = 2");
                break;
            case 5:
                System.out.println("i = 5");
                break;
            default:
                System.out.println("defoooolt");
        }

        System.out.println("End Program");
    }
}
